def print_header(text):
    lines = text.split('\n')
    max_length = max(len(line) for line in lines)
    print("\033[1;32m" + "=" * (max_length + 4) + "\033[0m")
    for line in lines:
        print("\033[1;32m" + f"| {line:<{max_length}} |" + "\033[0m")
    print("\033[1;32m" + "=" * (max_length + 4) + "\033[0m")

